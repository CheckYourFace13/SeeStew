/**
 * Remove internal pipeline fields from article JSON (not shown on site but kept out of repo).
 * Run: node scripts/sanitize-article-metadata.mjs
 */
import { readFileSync, readdirSync, writeFileSync } from "fs";
import { join } from "path";

const CONTENT_DIR = join(process.cwd(), "content", "articles");
const STRIP_TOP = ["autoGenerated", "queueId", "updatedAt", "imagePrompt"];

let changed = 0;

for (const file of readdirSync(CONTENT_DIR).filter((f) => f.endsWith(".json"))) {
  const path = join(CONTENT_DIR, file);
  const raw = readFileSync(path, "utf-8");
  const data = JSON.parse(raw);
  let dirty = false;

  for (const key of STRIP_TOP) {
    if (key in data) {
      delete data[key];
      dirty = true;
    }
  }

  if (data.image?.imagePrompt) {
    delete data.image.imagePrompt;
    dirty = true;
  }

  if (dirty) {
    writeFileSync(path, `${JSON.stringify(data, null, 2)}\n`, "utf-8");
    changed++;
    console.log(`  cleaned ${file}`);
  }
}

console.log(`\nDone. Updated ${changed} file(s).`);
